# [what is it ?]
# Unordered collection which is unordered, changeable and indexed.  (No duplicate items <key-value>)

# How to create one ?

students = {
    'Ahmed Asl': 2423252,
    'Mohamed': 9423453
}


# Get a Names of keys in dictionary
print(f"First  student Name {students.keys()[0]}")
print(f"Second student Name {students.keys()[1]}")


# Get a values for keys in dictionary
print(f"First student Id {students.values()[0]}")
print(f"Second student Id {students.values()[0]}")



# notice dictionary keys are immutable (can't be changed)

