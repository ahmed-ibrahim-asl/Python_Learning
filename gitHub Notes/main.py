#! important Terminologies
'''
1. Git -> It's a tool that developers use to track changes in their files over time.
          This tracking enables you to revert back to previous versions of your project if necessary,
          compare changes over time, and collaborate with others by merging changes from different
          branches and repositories.


[Repo (Repository)]: It's essentially a container for your project that tracks and records changes to your files over time.
This includes not just the current version of each file but also a history of all changes made to each
file. Repositories can be local or remote.

2. Github -> is a cloud-based hosting service for Git repositories. It takes the concept 
             of a Git repository and adds a layer of functionality that facilitates collaboration among developers.  


'''
